package sanctuary;

/**
 *  This documents the possible values of the size of a monkey,
 *  based on its size in centimeters.
 *  <p>
 *  If size in cm < 10 cm the MonkeySize is SMALL,
 *  If size in cm >= 10 cm and < 20 cm the MonkeySize is MEDIUM, and
 *  If size in cm >= 20 cm the MonkeySize is LARGE.
 *  </p>
 */
public enum MonkeySize {
  SMALL,
  MEDIUM,
  LARGE
}
